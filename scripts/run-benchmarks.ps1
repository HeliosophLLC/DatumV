<#
.SYNOPSIS
    Runs BenchmarkDotNet benchmarks and regenerates BENCHMARKS.md at the repo root.

.DESCRIPTION
    Cleans BenchmarkDotNet.Artifacts/results/ (override with -KeepPriorResults),
    runs `dotnet run -c Release` against benchmarks/Heliosoph.DatumV.Benchmarks, then
    globs the per-class `*-report-github.md` files BDN emits, extracts the shared
    environment block once, and appends one `## ClassName` section + table per
    class to BENCHMARKS.md (UTF-8, no BOM).

    BenchmarkDotNet.Artifacts/ is already gitignored; BENCHMARKS.md is tracked.
    A full real run of the current suite (ExecutionBenchmarks + HashSpillBenchmarks)
    takes roughly 30–60 minutes.

.PARAMETER Filter
    One or more BDN filter patterns. Defaults to `*` (whole suite). Accepts
    multiple values, e.g. `-Filter "*ExecutionBenchmarks*","*HashSpillBenchmarks*"`.

.PARAMETER KeepPriorResults
    Skip the initial wipe of BenchmarkDotNet.Artifacts/results/. Use when
    iterating class-by-class and you want the BENCHMARKS.md to accumulate
    across runs instead of replacing.

.PARAMETER Dry
    Adds `--job dry` — single iteration, no statistical rigor. Use to smoke-test
    that benchmarks execute, not for committable numbers.

.PARAMETER RegenerateOnly
    Skip the `dotnet run` step and only re-stitch BENCHMARKS.md from the existing
    `*-report-github.md` artifacts already on disk. Use after a script bug or
    formatting tweak to refresh the file without paying the full benchmark cost.

.PARAMETER ExtraArgs
    Anything after the named parameters is passed through to BenchmarkDotNet.

.EXAMPLE
    ./scripts/run-benchmarks.ps1
    Runs the whole suite and regenerates BENCHMARKS.md.

.EXAMPLE
    ./scripts/run-benchmarks.ps1 -Filter "*ExecutionBenchmarks*"
    Runs a single benchmark class.

.EXAMPLE
    ./scripts/run-benchmarks.ps1 -Filter "*ExecutionBenchmarks*","*HashSpillBenchmarks*"
    Runs multiple classes in one shot.

.EXAMPLE
    ./scripts/run-benchmarks.ps1 -Dry
    Fast smoke run — verifies benchmarks execute end-to-end.

.EXAMPLE
    ./scripts/run-benchmarks.ps1 -Filter "*Pivot*" -- --runtimes net10.0
    Passes additional arguments through to BenchmarkDotNet after `--`.
#>
[CmdletBinding()]
param(
    [string[]]$Filter = @("*"),
    [switch]$KeepPriorResults,
    [switch]$Dry,
    [switch]$RegenerateOnly,
    [Parameter(ValueFromRemainingArguments = $true)]
    [string[]]$ExtraArgs = @()
)

$ErrorActionPreference = "Stop"

$RepoRoot     = (Resolve-Path (Join-Path $PSScriptRoot "..")).Path
$BenchProject = Join-Path $RepoRoot "benchmarks/Heliosoph.DatumV.Benchmarks/Heliosoph.DatumV.Benchmarks.csproj"
$ArtifactsDir = Join-Path $RepoRoot "BenchmarkDotNet.Artifacts/results"
$OutputFile   = Join-Path $RepoRoot "BENCHMARKS.md"

Set-Location $RepoRoot

if (-not $RegenerateOnly) {
    if (-not $KeepPriorResults -and (Test-Path $ArtifactsDir)) {
        Remove-Item -Recurse -Force $ArtifactsDir
    }

    $cliArgs = @("--filter") + $Filter
    if ($Dry) { $cliArgs += @("--job", "dry") }
    if ($ExtraArgs) { $cliArgs += $ExtraArgs }

    Write-Host "Running: dotnet run -c Release -- $($cliArgs -join ' ')" -ForegroundColor Cyan
    & dotnet run --project $BenchProject -c Release --no-restore -- @cliArgs
    if ($LASTEXITCODE -ne 0) { throw "Benchmark run failed (exit $LASTEXITCODE)" }
}
else {
    Write-Host "RegenerateOnly: skipping dotnet run, re-stitching from existing artifacts." -ForegroundColor Cyan
}

$reports = @(Get-ChildItem $ArtifactsDir -Filter "*-report-github.md" -ErrorAction SilentlyContinue | Sort-Object Name)
if ($reports.Count -eq 0) { throw "No benchmark reports found in $ArtifactsDir" }

$sb = [System.Text.StringBuilder]::new()
[void]$sb.AppendLine("# Benchmark Results")
[void]$sb.AppendLine("")
[void]$sb.AppendLine("_Last run: $((Get-Date).ToString('u'))_")
[void]$sb.AppendLine("")
[void]$sb.AppendLine("Generated by ``scripts/run-benchmarks.ps1``. Re-run the script to refresh; do not edit by hand.")
[void]$sb.AppendLine("")

$envEmitted = $false
# BDN writes UTF-8; Get-Content on PS 5.1 defaults to ASCII/Windows-1252 detection,
# which mojibakes UTF-8 multi-byte chars (e.g. `→` becomes `→`).
# Read explicitly as UTF-8 so non-ASCII method names survive the stitch.
$utf8 = [System.Text.UTF8Encoding]::new($false)
foreach ($report in $reports) {
    $content = [System.IO.File]::ReadAllText($report.FullName, $utf8)
    $className = $report.BaseName -replace "^Heliosoph.DatumV\.Benchmarks\.", "" -replace "-report-github$", ""

    # BDN's per-class report is: ``` (env block) ``` then the markdown table.
    # Capture the env block once (identical across classes in a single run),
    # then strip it so only the table remains.
    if ($content -match '(?s)^```\s*\r?\n(.*?)\r?\n```\s*\r?\n') {
        if (-not $envEmitted) {
            [void]$sb.AppendLine("## Environment")
            [void]$sb.AppendLine("")
            [void]$sb.AppendLine('```')
            [void]$sb.AppendLine($matches[1].Trim())
            [void]$sb.AppendLine('```')
            [void]$sb.AppendLine("")
            $envEmitted = $true
        }
        $table = $content.Substring($matches[0].Length).Trim()
    } else {
        $table = $content.Trim()
    }

    [void]$sb.AppendLine("## $className")
    [void]$sb.AppendLine("")
    [void]$sb.AppendLine($table)
    [void]$sb.AppendLine("")
}

# Write UTF-8 without BOM so the file renders cleanly on GitHub and on disk diffs.
[System.IO.File]::WriteAllText($OutputFile, $sb.ToString(), $utf8)

Write-Host ""
Write-Host "BENCHMARKS.md updated: $OutputFile" -ForegroundColor Green
Write-Host "Classes included: $($reports.Count)"
